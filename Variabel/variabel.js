// Variabel dalam JavaScript adalah tempat untuk menyimpan data. Ada tiga cara mendeklarasikannya:

// var: Variabel global atau lokal dalam fungsi. Bisa diubah.
// let: Variabel dalam cakupan blok. Bisa diubah.
// const: Variabel yang nilainya tetap. Harus diinisialisasi saat deklarasi.
// Contoh:

let nama = "Rendi";
const umur = 19;

console.log(nama);
console.log(umur);

nama = "Hendra";
// umur = 20; Error karena variable umur constant(Tidak bisa di rubah)

console.log(nama);
console.log(umur);

// Variabel menyimpan berbagai jenis data seperti angka, teks, objek, dan array.
